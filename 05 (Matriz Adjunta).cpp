#include <stdio.h>
#include <locale.h>
#include <stdlib.h>

int main(){
	setlocale(LC_ALL,"Portuguese_Brazil");
	
	int a[3][3],adj[3][3];
	
	a[0][0]=1;
	a[0][1]=2;
	a[0][2]=3;
	a[1][0]=4;
	a[1][1]=5;
	a[1][2]=6;
	a[2][0]=7;
	a[2][1]=8;
	a[2][2]=9;
	
	adj[0][0]= (a[1][1]*a[2][2])-(a[2][1]*a[1][2]);// (5*9) - (8*6)
	adj[0][1]= (a[1][0]*a[2][2])-(a[2][0]*a[1][2]);// (4*9) - (7*6)
	adj[0][2]= (a[1][0]*a[2][1])-(a[2][0]*a[1][1]);// (4*8) - (7*5)
	adj[1][0]= (a[0][1]*a[2][2])-(a[2][1]*a[0][2]);// (2*9) - (8*3)
	adj[1][1]= (a[0][0]*a[2][2])-(a[2][0]*a[0][2]);// (1*9) - (7*3)
	adj[1][2]= (a[0][0]*a[2][1])-(a[2][0]*a[0][1]);// (1*8) - (7*2)
	adj[2][0]= (a[0][1]*a[1][2])-(a[1][1]*a[0][2]);// (2*6) - (5*3)
	adj[2][1]= (a[0][0]*a[1][2])-(a[1][0]*a[0][2]);// (1*6) - (4*3)
	adj[2][2]= (a[0][0]*a[1][1])-(a[1][0]*a[0][1]);// (1*5) - (4*2)
	
	printf("Matriz A\n");
	for(int i=0;i<3;i++){
		for(int j=0;j<3;j++){
			printf("%2d\t",a[i][j]);
		}
		printf("\n");
	}
	printf("\n\nMatriz Adjunta\n");
	for(int i=0;i<3;i++){
		for(int j=0;j<3;j++){
			printf("%2d\t",adj[i][j]);
		}
		printf("\n");
	}		
	return 0;
}

